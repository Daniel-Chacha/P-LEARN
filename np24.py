#sorting arrays
import numpy
a=numpy.array([3,2,0,1])
print(numpy.sort(a))

#sorting alphabetically
b=numpy.array(['banana','cherry','apple'])
print(numpy.sort(b))

#sort a boolean array
c=numpy.array([True,False,True])
print(numpy.sort(c))

#sorting 2D arrays
d=numpy.array([[3,2,4],[5,0,1]])
print(numpy.sort(d))